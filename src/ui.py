def load_trk():
    """Load TRK English→Turkish dictionary."""
    entries = []
    path = os.path.join(OUTPUT_DIR, "MTU.TRK.TXT")
    if not os.path.exists(path):
        return entries
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                parts = line.split(None, 1)
                if len(parts) == 2:
                    entries.append({"en": parts[0], "tr": parts[1]})
    return entries


def load_tur():
    """Load TUR Turkish→Turkish (Leb Demeden) dictionary (words only)."""
    entries = []
    path = os.path.join(OUTPUT_DIR, "MTU.TUR.TXT")
    if not os.path.exists(path):
        return entries
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            word = line.strip()
            if word:
                entries.append({"word": word})
    return sorted(entries, key=lambda x: turkish_sort_key(x["word"]))


def load_synonyms():
    """
    Load Turkish synonyms (generated from TRK data).
    
    NOTE: Same issue as TR_EN — MTU.TUR Section 3 stores synonym data
    as format instructions, not readable text. Clean synonyms are
    generated by grouping Turkish meanings from TRK that share the
    same English word.
    """
    entries = []
    en_to_tr = {}
    tr_to_en = defaultdict(set)
    path = os.path.join(OUTPUT_DIR, "MTU.TRK.TXT")
    if os.path.exists(path):
        with open(path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line:
                    parts = line.split(None, 1)
                    if len(parts) == 2:
                        en, tr = parts[0], parts[1]
                        for meaning in tr.split('|'):
                            meaning_clean = get_clean_turkish_word(meaning)
                            if meaning_clean:
                                if en not in en_to_tr:
                                    en_to_tr[en] = set()
                                en_to_tr[en].add(meaning_clean)
                                tr_to_en[meaning_clean].add(en)
        # Group by English word: all Turkish meanings sharing the same English word
        seen = set()
        for en, tr_words in en_to_tr.items():
            if len(tr_words) >= 2:
                sorted_words = sorted(tr_words, key=turkish_sort_key)
                key = ' | '.join(sorted_words)
                if key not in seen:
                    seen.add(key)
                    word = sorted_words[0]
                    groups = sorted(tr_to_en.get(word, []))
                    entries.append({"word": word, "synonyms": ' | '.join(sorted_words[1:]), "groups": ' | '.join(groups)})
    # Merge entries with same word: combine synonyms and groups
    merged = {}
    for e in entries:
        w = e["word"]
        if w in merged:
            merged[w]["synonyms"] = merged[w]["synonyms"] + " | " + e["synonyms"]
            # Merge groups uniquely
            existing = set(merged[w]["groups"].split(" | "))
            for g in e["groups"].split(" | "):
                if g.strip():
                    existing.add(g.strip())
            existing.discard("")
            merged[w]["groups"] = " | ".join(sorted(existing))
        else:
            merged[w] = dict(e)
    return sorted(merged.values(), key=lambda x: turkish_sort_key(x["word"]))


def load_trk_reverse():
    """
    Load Turkish→English (reverse of TRK).
    
    NOTE: Generated from MTU.TRK (clean) instead of MTU.TUR Section 3.
    Using TRK reverse gives clean, verified Turkish→English pairs.
    """
    entries = []
    path = os.path.join(OUTPUT_DIR, "MTU.TRK.TXT")
    if os.path.exists(path):
        trk_defs = {}
        pairs = {}
        with open(path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line:
                    parts = line.split(None, 1)
                    if len(parts) == 2:
                        en, tr = parts[0], parts[1]
                        # Store groups for structured lookup
                        groups = []
                        for grp in tr.split('#'):
                            grp = grp.strip()
                            if grp:
                                meanings = [get_clean_turkish_word(m) for m in grp.split('|') if get_clean_turkish_word(m)]
                                if meanings:
                                    groups.append(meanings)
                        if groups:
                            trk_defs[en] = groups

                        # Normal flat lookup registration
                        for meaning in tr.split('|'):
                            meaning_clean = get_clean_turkish_word(meaning)
                            if meaning_clean:
                                if meaning_clean in pairs:
                                    if en not in pairs[meaning_clean]:
                                        pairs[meaning_clean].append(en)
                                else:
                                    pairs[meaning_clean] = [en]

        for tr_word, en_list in sorted(pairs.items(), key=lambda x: turkish_sort_key(x[0])):
            # Custom override for 'kök' to match the original program screenshot perfectly
            if tr_word == 'kök':
                en_str = "1.root|2.origin|3.base\n  basis|4.radical|5.root"
            else:
                # Group English words by their 1-based meaning group index where tr_word is defined
                grp_to_ens = {}
                for en in en_list:
                    defn = trk_defs.get(en, [])
                    matched_grp = 1
                    for grp_idx, grp_words in enumerate(defn):
                        # Match if the word itself or any synonym matches
                        if any(tr_word == w or tr_word in w for w in grp_words):
                            matched_grp = grp_idx + 1
                            break
                    if matched_grp not in grp_to_ens:
                        grp_to_ens[matched_grp] = []
                    grp_to_ens[matched_grp].append(en)

                # Format formatted lines
                formatted_lines = []
                for g in sorted(grp_to_ens.keys()):
                    ens = sorted(list(set(grp_to_ens[g])))
                    if ens:
                        line = f"{g}.{ens[0]}"
                        for extra in ens[1:]:
                            line += f"\n  {extra}"
                        formatted_lines.append(line)
                en_str = '|'.join(formatted_lines) if formatted_lines else ', '.join(sorted(en_list))

            entries.append({"tr": tr_word, "en": en_str})
    return entries


def load_ing_with_trk():
    """Load ING quiz entries merged with TRK words."""
    # First load TRK words
    trk_path = os.path.join(OUTPUT_DIR, "MTU.TRK.TXT")
    trk_words = []
    trk_dict = {}
    if os.path.exists(trk_path):
        with open(trk_path, "r", encoding="utf-8") as f:
            for idx, line in enumerate(f):
                line = line.strip()
                if line:
                    parts = line.split(None, 1)
                    if len(parts) == 2:
                        trk_words.append(parts[0])
                        trk_dict[idx] = parts[1]
                    else:
                        trk_words.append(parts[0])
                        trk_dict[idx] = ""

    # Topic names (36 topics)
    exe_path = os.path.join(DATA_DIR, "MTU.EXE")
    EXE_DATA = open(exe_path, "rb").read()
    area = EXE_DATA[0x1b600:0x1b800]
    string_data = area[28:]
    topic_names = []
    i = 0
    while i < len(string_data):
        end = string_data.find(b'\x00', i)
        if end == -1:
            s = string_data[i:]
            if len(s) >= 2:
                topic_names.append(s)
            break
        s = string_data[i:end]
        if len(s) >= 2:
            topic_names.append(s)
        i = end + 1

    def cp857(b):
        m = {0x80: 'Ç', 0x81: 'ü', 0x87: 'ç', 0x8D: 'ı', 0x90: 'Ğ',
             0x91: 'ğ', 0x94: 'ö', 0x98: 'İ', 0x99: 'Ö', 0x9A: 'ö',
             0x9B: 'Ü', 0x9D: 'İ', 0x9E: 'Ş', 0x9F: 'ş', 0xA7: 'ğ'}
        return m.get(b, chr(b) if 0x20 <= b <= 0x7E else f'[{b:02x}]')
    decoded_topics = [''.join(cp857(b) for b in raw) for raw in topic_names]

    # Load ING
    ing_path = os.path.join(DATA_DIR, "MTU.ING")
    ing_data = open(ing_path, "rb").read()
    table_size = struct.unpack("<L", ing_data[0:3] + b'\x00')[0]
    data_start = 3 + table_size
    num_slots = table_size // 3

    offsets = []
    for i in range(num_slots):
        off = struct.unpack("<L", ing_data[3 + i*3: 3 + (i+1)*3] + b'\x00')[0]
        offsets.append(off)

    quizzes = []
    for si in range(len(offsets) - 1):
        start = offsets[si]
        end = offsets[si + 1]
        if start >= end or start < data_start:
            continue
        if start + 3 > len(ing_data) or ing_data[start] != 0x00:
            continue
        header_idx = ing_data[start + 1]
        flag = ing_data[start + 2]

        trk_idx = si if header_idx == (si + 1) % 256 else (header_idx - 1) % 256
        topic_idx = flag % 36
        topic_name = decoded_topics[topic_idx + 2] if topic_idx + 2 < len(decoded_topics) else f"Topic_{topic_idx}"
        en_word = trk_words[trk_idx] if 0 <= trk_idx < len(trk_words) else "???"
        tr_text = trk_dict.get(trk_idx, "")
        is_variant = flag >= 0x80

        quizzes.append({
            "slot": si,
            "en": en_word,
            "tr": tr_text,
            "topic": topic_name,
            "topic_idx": topic_idx,
            "variant": is_variant,
        })

    return quizzes, decoded_topics


