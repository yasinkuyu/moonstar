def main():
    import subprocess
    pid = subprocess.run(['lsof', '-ti', str(PORT)], capture_output=True, text=True).stdout.strip()
    if pid:
        print(f"  ⚠ Port {PORT} kullanımda (PID:{pid}), eski process sonlandırılıyor…")
        subprocess.run(['kill', '-9', pid], capture_output=True)
        time.sleep(0.3)

    http.server.HTTPServer.allow_reuse_address = True
    server = http.server.HTTPServer(("0.0.0.0", PORT), MoonStarHandler)

    def shutdown(signum=None, frame=None):
        print("\nSunucu durduruldu.")
        server.shutdown()
        server.server_close()
        sys.exit(0)

    signal.signal(signal.SIGTERM, shutdown)

    print(f"\n🌙 MoonStar Veri Gezgini çalışıyor:")
